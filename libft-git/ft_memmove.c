#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char	*d;
	unsigned char	*s;

	d = (unsigned char *)dst;
	s = (unsigned char *)src;
	if (!dst && !src && len > 0)
		return (NULL);
	if (!dst || !src)
	{
		if (len > 0)
			return (NULL);
		return (dst);
	}
	if (d > s && d < (s + len))
	{
		d = d + len;
		s += len;
		while (len--)
			*--d = *--s;
	}
	else
		while (len--)
			*d++ = *s++;
	return (dst);
}
